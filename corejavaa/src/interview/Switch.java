package interview;

public class Switch {
	public static void main(String[] args) {
		int choice =10;
		switch (choice){
		case 10:System.out.println("value is 10");
		break;
		case 11:System.out.println("value is 11");
		break;
		case 12:System.out.println("value is 12");
		break;
		//default:System.out.println("not an integer");
		}
	}
	

}
