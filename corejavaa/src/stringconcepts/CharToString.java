package stringconcepts;

public class CharToString {
	  public static void main(String args[]) 
	    {
	        String a = new String("sample");
	        System.out.println("Result: ");
	        for(int i=0;i<a.length();i++)
	        {
	            System.out.print(a.charAt(i));
	        }
	    }
	}

